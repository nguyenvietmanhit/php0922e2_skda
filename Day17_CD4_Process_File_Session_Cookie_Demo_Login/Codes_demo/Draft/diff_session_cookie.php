- Là các biến dạng mảng có tính chất khởi tạo 1 lần và truy cập
từ mọi nơi trên hệ thống
+ Khác nhau:
- Session phải khởi tạo thì mới sử dụng đc, cookie thì ko
- Session lưu trên server, cookie lưu trên trình duyệt -> session
bảo mật hơn cookie
- Session tự mất đi khi đóng trình duyệt, cookie thì ko